#include "fillit.h"

void	swap_rows(char a[5][6])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (a[0][0] == '.' && a[0][1] == '.' && a[0][2] == '.' && a[0][3] == '.')
	{
		while (j < 4)
		{
			while (i < 4)
			{
				a[i][j] = a[i + 1][j];
				i++;
			}
			a[--i][j] = '.';
			i = 0;
			j++;
		}
		j = 0;
	}
}
